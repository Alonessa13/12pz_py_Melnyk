# Список міст
cities = ["Київ", "Одеса", "Львів", "Харків", "Житомир"]

# Перетворення списку в множину для швидшого пошуку
city_set = set(cities)

# Перевірка наявності міст
for city in ["Одеса", "Полтава"]:
    if city in city_set:
        print(f"{city} є у списку.")
    else:
        print(f"{city} відсутнє у списку.")
